/* wrap a constant pointer */
void wCreatePtrConst( char *name, int classIndex, int pointer )
{
    int  handle;

    /* wrap the pointer */
    handle = (int)wAddObject( classIndex, pointer );

    /* create as a normal constant */
    wCreateNumConst( name, handle );
}

